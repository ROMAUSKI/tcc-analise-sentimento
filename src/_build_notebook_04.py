    "from sklearn.preprocessing import LabelEncoder\n"
    "from sklearn.metrics import accuracy_score, precision_recall_fscore_support, f1_score\n"
    "\n"
    "def run_lstm_vision(X_tr, y_tr, X_te, y_te, visao_nome):\n"
