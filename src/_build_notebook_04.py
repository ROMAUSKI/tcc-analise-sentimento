cells.append(nbf.v4.new_code_cell(
    "# ============================================================\n"
    "# CÉLULA 10 — BERT: tokenizer + dataset\n"
    "# ============================================================\n"
    "from transformers import AutoTokenizer, AutoModelForSequenceClassification, Trainer, TrainingArguments\n"
    "from transformers import DataCollatorWithPadding\n"
    "\n"
    "print(f'Carregando tokenizer {BERT_MODEL_NAME}...')\n"
    "BERT_TOKENIZER = AutoTokenizer.from_pretrained(BERT_MODEL_NAME)\n"
    "print(f'  vocab_size: {BERT_TOKENIZER.vocab_size}, max_len recomendado: {BERT_TOKENIZER.model_max_length}')\n"
    "\n"
    "class BertDataset(torch.utils.data.Dataset):\n"
    "    def __init__(self, textos, labels, tokenizer, max_length):\n"
    "        self.encodings = tokenizer(\n"
    "            textos.tolist() if hasattr(textos, \"tolist\") else list(textos),\n"
    "            truncation=True, padding=False, max_length=max_length\n"
    "        )\n"
    "        self.labels = labels.values if hasattr(labels, 'values') else list(labels)\n"
    "    def __len__(self): return len(self.labels)\n"
    "    def __getitem__(self, idx):\n"
    "        item = {k: torch.tensor(v[idx]) for k, v in self.encodings.items()}\n"
    "        item['labels'] = torch.tensor(int(self.labels[idx]))\n"
    "        return item\n"
))

