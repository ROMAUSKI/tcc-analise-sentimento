cells.append(nbf.v4.new_code_cell(
    "# ============================================================\n"
    "# CÉLULA 7 — LSTM: arquitetura + helpers de tokenização\n"
    "# ============================================================\n"
    "import torch.nn as nn\n"
    "from torch.utils.data import Dataset, DataLoader\n"
    "from collections import Counter\n"
    "\n"
    "PAD, UNK = 0, 1\n"
    "\n"
    "def tokenize(texto):\n"
    "    \"\"\"Tokenização simples por palavra (split em espaço)\"\"\"\n"
    "    return texto.split()\n"
    "\n"
    "def build_vocab(textos, min_freq=1, max_size=20000):\n"
    "    \"\"\"Constrói vocabulário com índices reservados PAD=0, UNK=1\"\"\"\n"
    "    counter = Counter()\n"
    "    for t in textos:\n"
    "        counter.update(tokenize(t))\n"
    "    vocab = {'<PAD>': PAD, '<UNK>': UNK}\n"
    "    for word, freq in counter.most_common(max_size - 2):\n"
    "        if freq >= min_freq:\n"
    "            vocab[word] = len(vocab)\n"
    "    return vocab\n"
    "\n"
    "def encode(texto, vocab, max_len):\n"
    "    \"\"\"Converte texto em lista de índices, padded/truncated para max_len\"\"\"\n"
    "    ids = [vocab.get(w, UNK) for w in tokenize(texto)[:max_len]]\n"
    "    length = len(ids)\n"
    "    ids = ids + [PAD] * (max_len - length)\n"
    "    return ids, length\n"
    "\n"
    "class TextDataset(Dataset):\n"
    "    def __init__(self, textos, labels, vocab, max_len):\n"
    "        self.data = [encode(t, vocab, max_len) for t in textos]\n"
    "        self.labels = labels.values if hasattr(labels, 'values') else list(labels)\n"
    "    def __len__(self): return len(self.data)\n"
    "    def __getitem__(self, idx):\n"
    "        ids, length = self.data[idx]\n"
    "        return torch.tensor(ids, dtype=torch.long), torch.tensor(length, dtype=torch.long), self.labels[idx]\n"
    "\n"
    "class LSTMClassifier(nn.Module):\n"
    "    def __init__(self, vocab_size, embed_dim, hidden_dim, num_classes, dropout):\n"
    "        super().__init__()\n"
    "        self.embedding = nn.Embedding(vocab_size, embed_dim, padding_idx=PAD)\n"
    "        self.lstm = nn.LSTM(embed_dim, hidden_dim, batch_first=True)\n"
    "        self.dropout = nn.Dropout(dropout)\n"
    "        self.fc = nn.Linear(hidden_dim, num_classes)\n"
    "    def forward(self, x, lengths):\n"
    "        emb = self.embedding(x)\n"
    "        packed = nn.utils.rnn.pack_padded_sequence(emb, lengths.cpu(), batch_first=True, enforce_sorted=False)\n"
    "        _, (h, _) = self.lstm(packed)\n"
    "        h = self.dropout(h.squeeze(0))\n"
    "        return self.fc(h)\n"
    "\n"
    "print('LSTM definido. Pronto para treino.')\n"
))

