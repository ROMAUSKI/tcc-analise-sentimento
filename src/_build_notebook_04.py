# ============================================================
cells.append(nbf.v4.new_code_cell(
    "# ============================================================\n"
    "# CÉLULA 11 — Função run_bert_vision(X_tr, y_tr, X_te, y_te, visao_nome)\n"
    "# ============================================================\n"
    "import warnings\n"
    "warnings.filterwarnings('ignore')\n"
    "\n"
    "def compute_metrics(eval_pred):\n"
    "    logits, labels = eval_pred\n"
    "    preds = logits.argmax(axis=-1)\n"
    "    acc = accuracy_score(labels, preds)\n"
    "    prec, rec, f1w, _ = precision_recall_fscore_support(labels, preds, average='weighted', zero_division=0)\n"
    "    f1m = f1_score(labels, preds, average='macro', zero_division=0)\n"
    "    return {'accuracy': acc, 'precision': prec, 'recall': rec, 'f1_weighted': f1w, 'f1_macro': f1m}\n"
    "\n"
    "def run_bert_vision(X_tr, y_tr, X_te, y_te, visao_nome):\n"
    "    print(f'\\n=== BERT | {visao_nome} ===')\n"
    "    print(f'  Treino: {len(X_tr):,} frases | Teste: {len(X_te):,} frases')\n"
    "    t0 = time.time()\n"
    "\n"
    "    le = LabelEncoder()\n"
    "    y_tr_enc = le.fit_transform(y_tr)\n"
    "    y_te_enc = le.transform(y_te)\n"
    "    num_classes = len(le.classes_)\n"
    "\n"
    "    train_ds = BertDataset(X_tr, pd.Series(y_tr_enc), BERT_TOKENIZER, BERT_MAX_LENGTH)\n"
    "    test_ds  = BertDataset(X_te, pd.Series(y_te_enc), BERT_TOKENIZER, BERT_MAX_LENGTH)\n"
    "\n"
    "    model = AutoModelForSequenceClassification.from_pretrained(BERT_MODEL_NAME, num_labels=num_classes).to(DEVICE)\n"
    "\n"
    "    safe_name = re.sub(r'[^a-zA-Z0-9]+', '_', visao_nome).lower()\n"
    "    out_dir = os.path.join(CHECKPOINTS, f'bert_{safe_name}')\n"
    "\n"
    "    training_args = TrainingArguments(\n"
    "        output_dir=out_dir,\n"
    "        num_train_epochs=BERT_EPOCHS,\n"
    "        per_device_train_batch_size=BERT_BATCH_SIZE,\n"
    "        per_device_eval_batch_size=BERT_BATCH_SIZE * 2,\n"
    "        learning_rate=BERT_LR,\n"
    "        weight_decay=BERT_WEIGHT_DECAY,\n"
    "        eval_strategy='epoch',\n"
    "        save_strategy='epoch',\n"
    "        save_total_limit=1,                # mantém só o último checkpoint\n"
    "        load_best_model_at_end=False,\n"
    "        logging_steps=200,\n"
    "        seed=SEED,\n"
    "        report_to='none',\n"
    "        fp16=torch.cuda.is_available(),    # mixed precision se GPU\n"
    "    )\n"
    "\n"
    "    trainer = Trainer(\n"
    "        model=model,\n"
    "        args=training_args,\n"
    "        train_dataset=train_ds,\n"
    "        eval_dataset=test_ds,\n"
    "        tokenizer=BERT_TOKENIZER,\n"
    "        data_collator=DataCollatorWithPadding(BERT_TOKENIZER),\n"
    "        compute_metrics=compute_metrics,\n"
    "    )\n"
    "\n"
    "    trainer.train()\n"
    "    final_metrics = trainer.evaluate()\n"
    "    elapsed = time.time() - t0\n"
    "\n"
    "    f1w = final_metrics['eval_f1_weighted']\n"
    "    f1m = final_metrics['eval_f1_macro']\n"
    "    acc = final_metrics['eval_accuracy']\n"
    "    print(f'  ✓ BERT | F1w={f1w:.2%}  F1macro={f1m:.2%}  Acc={acc:.2%}  ({elapsed/60:.1f}min)')\n"
    "\n"
    "    # Libera VRAM\n"
    "    del model, trainer\n"
    "    if torch.cuda.is_available(): torch.cuda.empty_cache()\n"
    "\n"
    "    return {'Visão': visao_nome, 'Modelo': 'BERT (Bertimbau)',\n"
    "            'Acurácia': acc, 'Precisão': final_metrics['eval_precision'],\n"
    "            'Recall': final_metrics['eval_recall'],\n"
    "            'F1-Score': f1w, 'F1-Macro': f1m, 'Tempo (s)': round(elapsed, 1)}\n"
))

