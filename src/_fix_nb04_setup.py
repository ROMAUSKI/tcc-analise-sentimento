    "    pkgs_a_instalar = []\n"
    "    if not _check_module('transformers'): pkgs_a_instalar.append('transformers>=4.40')\n"
    "    if not _check_module('accelerate'):   pkgs_a_instalar.append('accelerate>=0.30')\n"
    "    if not _check_module('kagglehub'):    pkgs_a_instalar.append('kagglehub>=1.0.1')\n"
    "    if pkgs_a_instalar:\n"
    "        _pip_install(pkgs_a_instalar)\n"
    "        print(f'  Instalados: {pkgs_a_instalar}')\n"
