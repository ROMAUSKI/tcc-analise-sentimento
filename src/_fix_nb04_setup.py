    "    cmd = [sys.executable, '-m', 'pip', 'install'] + (['-q'] if quiet else []) + pkgs\n"
    "    subprocess.run(cmd, check=True)\n"
    "\n"
    "def _check_module(name):\n"
    "    \"\"\"Retorna True se o módulo importa.\"\"\"\n"
    "    try:\n"
    "        importlib.import_module(name)\n"
    "        return True\n"
    "    except ImportError:\n"
    "        return False\n"
    "\n"
    "# ============================================================\n"
    "# CASO 1 — Google Colab\n"
