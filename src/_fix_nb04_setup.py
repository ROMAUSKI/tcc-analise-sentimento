    "        print('   pip install torch --index-url https://download.pytorch.org/whl/cu121 --upgrade')\n"
    "        print('   E reinicie o kernel do notebook.')\n"
    "    print('   Treinar BERT em CPU é INVIÁVEL (semanas). Use Colab com runtime GPU se necessário.')\n"
