    "# ============================================================\n"
    "else:\n"
    "    falta_torch = not _check_module('torch')\n"
    "    falta_tx    = not _check_module('transformers')\n"
    "    falta_acc   = not _check_module('accelerate')\n"
    "    falta_kh    = not _check_module('kagglehub')\n"
    "\n"
    "    # Pacotes não-torch são fáceis: instala direto\n"
    "    pkgs_simples = []\n"
