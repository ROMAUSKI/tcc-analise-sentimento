    "    if falta_acc: pkgs_simples.append('accelerate>=0.30')\n"
    "    if falta_kh:  pkgs_simples.append('kagglehub>=1.0.1')\n"
    "    if pkgs_simples:\n"
    "        print(f'Instalando localmente: {pkgs_simples}')\n"
    "        _pip_install(pkgs_simples, quiet=False)\n"
    "\n"
    "    # PyTorch precisa de cuidado: usuário deve escolher CUDA vs CPU\n"
    "    if falta_torch:\n"
    "        print('\\n' + '='*70)\n"
    "        print('❌ PyTorch NÃO está instalado. Instale ANTES de prosseguir:')\n"
    "        print('='*70)\n"
    "        print('\\nPara GPU NVIDIA (RTX 3000+, recomendado para BERT):')\n"
    "        print('   pip install torch --index-url https://download.pytorch.org/whl/cu121')\n"
    "        print('\\nPara CPU apenas (BERT vai levar DIAS — só use se não tem GPU):')\n"
    "        print('   pip install torch')\n"
    "        print('\\nDepois reinicie o kernel do notebook e execute esta célula novamente.')\n"
    "        print('='*70)\n"
    "        raise SystemExit('Instale PyTorch e reinicie o kernel.')\n"
    "\n"
    "# ============================================================\n"
    "# Verificação final: PyTorch + CUDA\n"
    "# ============================================================\n"
    "import torch\n"
